public class Human {
    int age;
    int weight;
    int height;
    String name;
    String male;

    public Human(int age, int weight, int height, String name, String male) {
        this.age = age;
        this.weight = weight;
        this.height = height;
        this.name = name;
        this.male = male;
    }

    public int getAge() {
        return age;
    }

    public int getWeight() {
        return weight;
    }

    public int getHeight() {
        return height;
    }

    public String getName() {
        return name;
    }

    public String getMale() {
        return male;
    }



}
